def _fill_missing_languages(message_dict, fallback_language="en"):
    if not message_dict:
        return
    if fallback_language not in message_dict:
        fallback_language = "fr" if "fr" in message_dict else next(iter(message_dict))
    for lang in SUPPORTED_LANGUAGE_CODES:
        if lang not in message_dict:
            message_dict[lang] = deepcopy(message_dict[fallback_language])


